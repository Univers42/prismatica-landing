export const THEME_MAP = new Map(TERMINAL_THEMES.map((t) => [t.id, t]));

/** Get a theme by ID, falling back to Inferno */
export function getTheme(id: string): TerminalThemeDef {
  return THEME_MAP.get(id) ?? THEME_INFERNO;
}

/** Apply a theme's tokens to a DOM element as CSS custom properties */
export function applyThemeTokens(el: HTMLElement, theme: TerminalThemeDef): void {
  for (const [prop, value] of Object.entries(theme.tokens)) {
    el.style.setProperty(prop, value);
  }
}
