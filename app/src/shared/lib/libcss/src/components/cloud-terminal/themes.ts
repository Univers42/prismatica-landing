/**
 * @file themes.ts
 * @description 12 terminal themes with CSS token maps, xterm.js colors,
 *              and background gradient definitions.
 *
 * Each theme is fully self-contained — swap themes by applying the
 * token map to CSS custom properties and passing xterm colors to the Terminal.
 *
 * Part of the CloudTerminal configurable theme system.
 */

import type { TerminalThemeColors } from './CloudTerminal.types';

export interface TerminalThemeDef {
  /** Unique theme ID */
  readonly id: string;
  /** Human-readable name */
  readonly name: string;
  /** Short description */
  readonly description: string;
  /** CSS custom property overrides (--ct-*) */
  readonly tokens: Readonly<Record<string, string>>;
  /** xterm.js colour palette */
  readonly xterm: TerminalThemeColors;
  /** Background orb gradients (for TerminalBackground) */
  readonly orbs: {
    readonly primary: string;
    readonly secondary: string;
    readonly tertiary: string;
  };
  /** Glassmorphism intensity: 0 = none, 1 = heavy */
  readonly glass: number;
  /** Preview gradient (for theme picker swatch) */
  readonly preview: string;
}

